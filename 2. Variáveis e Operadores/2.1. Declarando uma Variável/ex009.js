// Operadores relacionais

// Os operadores de comparação são: ==, !=, <, >, <=, >=, === e !==.

// == (igual) != (diferente) === (igual estrita (só aceita se o valor e o tipo forem iguais)) !=== (diferença estrita)
console.log (10 == 10);
console.log (10 != 10);
console.log ("10" === 10);
console.log ("10" === "10");
console.log ("10" !== 10);
console.log ("10" !== "10");

//  > (maior que) < (menor que) >= (maior e igual que) <= (menor igual que)

13 < 100 // true
34 < 5 // false

20 > 10 // true
20 > 50 // false

13 <= 100 // true
13 <= 13 // true
34 <= 5 // false
34 <= 34 // true

48 >= 48 // true
30 >=31 // false
