// if / else

// A estrutura condicional trabalha com estrutura booleanas ou seja true e false.

// Exemplos

if ( limite >= valor) {
    console.log ("Compra aprovada")
}
else {
    console.log ("Compra negada")
}


if ( reporta != 3) {
    console.log ('Reposta errada')
}
else {
    console.log ("Resposta certa")
}


if ( idade >= 18 && idade <= 24) {
    console.log ("Você está na faixa etária entre 18 e 24 anos")
}
else {
    console.log ("Você não está na faixa etária")
}

if ( reposta == 32) {
    console.log ("Reposta correta")
}
else {
    console.log ("Reposta errada")
}
